#include "libft.h"

char	*ft_strtrim(const char *s1, const char *set)
/*Alloue (avec malloc(3)) et retourne une copie de la chaine 's1', 
sans les caracteres specifies dans 'set' au debut et a la fin de la chaine de caracteres
Retourne la chaine de caracteres trimmee.NULL si l'allocation echoue.*/
{
	size_t	i;

	if (!s1 || !set)
		return (0);
	while (*s1 && ft_strchr(set, *s1))
		s1++;
	i = ft_strlen(s1);
	while (i && ft_strchr(set, s1[i]))
		i--;
	return (ft_substr(s1, 0, i + 1));
}
