#include "libft.h"

char	*ft_strjoin(const char *s1, const char *s2)
/*Alloue (avec malloc(3)) et retourne une nouvelle chaine, 
resultat de la concatenation de s1 et s2.
Retourne la nouvelle chaine de caracteres. NULL si l'allocation echoue.*/
{
	char	*str;
	size_t	i;
	size_t	j;

	if (s1 == NULL || s2 == NULL)
		return (NULL);
	str = malloc(sizeof(char) * (ft_strlen(s1) + ft_strlen(s2) + 1));
	if (!str)
		return (NULL);
	i = 0;
	j = 0;
	while (s1[i])
	{
		str[j++] = s1[i];
		i++;
	}
	i = 0;
	while (s2[i])
	{
		str[j++] = s2[i];
		i++;
	}
	str[j] = 0;
	return (str);
}
