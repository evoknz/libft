#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
/*Ecrit l'integer 'n' sur le file descriptor donne
Retourne none.*/
{
	unsigned int	nbr;

	if (n < 0)
	{
		nbr = -n;
		ft_putchar_fd('-', fd);
	}
	else
		nbr = n;
	if (nbr >= 10)
		ft_putnbr_fd(nbr / 10, fd);
	ft_putchar_fd(nbr % 10 + '0', fd);
}
